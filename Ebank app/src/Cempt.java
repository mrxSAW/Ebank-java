public class Cempt {

    private  String cemptN;
    private  double solde ;
    private  String CIN ;

    public Cempt (String cemptN,double solde ,String CIN) {
        this.cemptN = cemptN;
        this.solde = solde;
        this.CIN = CIN;
    }


    public String getCemptN() {
        return cemptN;
    }

    public double getSolde() {
        return solde;
    }

    public String getCIN() {
        return CIN;
    }

    public void setCemptN(String cemptN) {
        this.cemptN = cemptN;
    }

    public void setSolde(double solde) {
        this.solde = solde;
    }

    public void setCIN(String CIN) {
        this.CIN = CIN;
    }






}








